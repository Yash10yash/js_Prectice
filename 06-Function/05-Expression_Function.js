// Expression function 
// Expression function is a function that is assigned to a variable. It can be anonymous or named.

// Example of an anonymous function assigned to a variable
let greet = function() {
    console.log("Hello, World!");
}
greet(); // This will call the expression function and print the message to the console.

// Example of a named function assigned to a variable
let add = function sum(a, b) {
    return a + b;
}
add(5, 3); // This will call the expression function and return the sum of 5 and 3.