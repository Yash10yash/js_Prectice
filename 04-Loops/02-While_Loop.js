// Sum of numbers from 1 to 10 using while loop
let sum = 0;
let i = 1;  
while (i <= 10) {
    sum += i;   
    i++; // Incrementing i by 1 in each iteration
}
console.log("Sum of numbers from 1 to 10:", sum);