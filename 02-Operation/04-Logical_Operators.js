let a = 10;
let b = 5;
let c = 15;
console.log("Logical Operators:");
console.log("a > b && a < c:", a > b && a < c); // true (both conditions are true)
console.log("a > b || a > c:", a > b || a > c); // true (at least one condition is true)
console.log("! (a < b):", !(a < b)); // true (the opposite of false)