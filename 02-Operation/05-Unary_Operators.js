let x = 10; 
console.log("Initial value of x:", x); // 10

// Unary plus operator
let unaryPlus = +x; 
console.log("Unary plus applied to x:", unaryPlus); // 10
// Unary minus operator
let unaryMinus = -x; 
console.log("Unary minus applied to x:", unaryMinus); // -10        
// Increment operator (postfix)
let postfixIncrement = x++; 0
console.log("Value of x after postfix increment (x++):", postfixIncrement); // 10
console.log(x);
// Decrement operator (postfix)
let postfixDecrement = x--; 
console.log("Value of x after postfix decrement (x--):", postfixDecrement); // 11
// Increment operator (prefix)
let prefixIncrement = ++x; 
console.log("Value of x after prefix increment (++x):", prefixIncrement); // 11   
// Decrement operator (prefix)
let prefixDecrement = --x; 
console.log("Value of x after prefix decrement (--x):", prefixDecrement); // 10