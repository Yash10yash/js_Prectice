let a = 10;
let b = 5;
console.log("Comparison Operators:");
console.log("a == b:", a == b); // false isEqual to only value
console.log("a === b:", a === b); // false isEqual value and type
console.log("a != b:", a != b); // true  not equal to only value
console.log("a !== b:", a !== b); // true not equal value or type   
console.log("a > b:", a > b); // true   a is greater than b
console.log("a < b:", a < b); // false  b is less than a
console.log("a >= b:", a >= b); // true  a is greater than or equal to b
console.log("a <= b:", a <= b); // false   a is less than or equal to b